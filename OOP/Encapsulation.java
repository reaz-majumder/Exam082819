package OOP;

public class Encapsulation {
    private String name;
    private int id;

    //Getter
    public String getName() {
        return name;
    }

    public int getId() {
        return id;
    }

    //Setter
    public void setName(String newName) {
        name = newName;
    }
    public void setId(int newId){
        id = newId;
    }

}

